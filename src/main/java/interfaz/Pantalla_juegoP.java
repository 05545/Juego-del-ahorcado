    public void iniciar_juego(int dificultad) {

        String nivel = "";
        switch (dificultad) {
            case 1:
                nivel = "facil";
                break;
            case 2:
                nivel = "medio";
                break;
            case 3:
                nivel = "dificil";
                break;
        }
        Gestor_palabras gestor = new Gestor_palabras();
        Palabra palabra1 = gestor.obtenerP_Aleatoria(nivel);
        juego = new Juego(palabra1.obtener(), 9);
        this.palabra = palabra1;
        actualizar_estado();
    }

