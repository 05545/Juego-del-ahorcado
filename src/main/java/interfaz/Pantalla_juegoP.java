    private void LetraRecibida(char letra) {

        if (juego.adivinar_letra(letra)) {
            actualizar_estado();
        } else {
            actualizar_estado();
        }

        if (juego.juego_terminado()) {
            if (juego.juego_ganado()) {
                FinJuego.setVisible(true);
                btnfinregreso.setVisible(true);
                jugador.incrementar_puntuacion();
            } else {
                FinJuegoMal.setVisible(true);
                btnfinregreso.setVisible(true);
            }
            JOptionPane.showMessageDialog(this, "");

            this.dispose();
            Pantalla_incio PIN = new Pantalla_incio();
            PIN.setVisible(true);
            PIN.setLocationRelativeTo(null);
        }
    }

